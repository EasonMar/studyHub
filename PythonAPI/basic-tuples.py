## ---- 切片：元组也是一个序列。
print([1,2,3][0])
print((1,2,3)[-1])
print((1,2,3)[0:2])
print((1,[1,2,3])[1][1])


## ---- 元组操作：列表中不改变对象值的方法，大部分都被元组继承。

# 长度
print(len((1,2,3)))

# 索引位置
print((1,2,3).index(1))