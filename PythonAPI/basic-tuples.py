## ---- 元组：元组是一个更加安全的列表。元组跟列表很像，只是定义好了以后，里面的数据就不能修改。

print(type(())) # <type 'tuple'> 元组用 () 表示;  ---- 注: python3 中输出为<class 'tuple'>

##  ---- 定义元组
t = (1,'a',{},[],True)
print(t)

##  ---- 避免歧义
# t = (1,) # 当元组只有一位时后面跟上逗号，避免歧义)
